import turtle
tu = turtle.Pen()
tu.shape("turtle")
tu.color("green")
tu.stamp()
move=30
for i in range(12):
    tu.penup()
    tu.forward(50)
    tu.pendown
    tu.forward(25)
    tu.penup()
    tu.forward(15)
    tu.stamp()
    tu.home()
    tu.right(move)
    move=move+30

tu.done
